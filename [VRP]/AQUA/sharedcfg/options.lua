AQUAConfig = {} -- Global variable for easy referencing. 

AQUAConfig.MoneyUiEnabled = false; -- Set to false to disable Money in the top right corner. 
AQUAConfig.SurvivalUiEnabled = false; -- Controls the UI under the healthbar.
AQUAConfig.EnableComa = true; -- Controls the AQUA coma on death.
AQUAConfig.EnableFoodAndWater = false; -- Controls the food and water system.
AQUAConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
AQUAConfig.EnableBuyVehicles = false; -- Enables ability to buy vehicles from the RageUI Garages.  
AQUAConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
AQUAConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
AQUAConfig.AdminCoolDown = true; -- Enables an admin cooldown on call admin.
AQUAConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
AQUAConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
AQUAConfig.DoNotDisplayIps = true; -- Removes all AQUA related references in the console to player ip addresses.
AQUAConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
AQUAConfig.AllowMoreThenOneCar = true; -- Controls if you can have more than one car out.
AQUAConfig.F10System = true; -- Logs warnings and can be accessed via F10 (Thanks to Rubbertoe98) (https://github.com/rubbertoe98/FiveM-Scripts/tree/master/AQUA_punishments)
AQUAConfig.ServerName = "AQUA" -- Controls the name that is displayed on warnings title etc.
AQUAConfig.PlayerSavingTime = 3000 -- Time in milliseconds to update Player saving
---------------
AQUAConfig.LootBags = true; -- Enables loot bags and disables looting. 
AQUAConfig.DisplayNamelLootbag = true; -- Enables notification of who's lootbag you have opened
